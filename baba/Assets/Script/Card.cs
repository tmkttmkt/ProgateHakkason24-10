using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Card : MonoBehaviour
{
    public int number; // カードの数字 (1〜13: スート, 14: ジョーカー)
    public string suit; // カードのスート (e.g. "Hearts", "Diamonds", etc.)

    void Start()
    {

    }

    // Update is called once per frame
    void Update()
    {

    }

    // カードのペア判定
    public bool IsPair(Card other) // publicを追加
    {
        return this.number == other.number;
    }
}
